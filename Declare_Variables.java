public class Declare_Variables{
    public static void main(String[] args){
        //Integer numbers
        int num=10;
        System.out.println(num);

        //Floating numbers
        float f=20.3f;
        System.out.println(f);

        //Character
        char ch='M';
        System.out.println(ch);

        //String
        String str="Malyadri";
        System.out.println(str);

        //Boolean
        boolean val1=true;
        boolean val2=false;
        System.out.println(val1);
        System.out.println(val2);
    }
}